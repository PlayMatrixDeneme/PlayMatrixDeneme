export function getHomeGameByKey(key = '') {
  const normalized = String(key || '').trim().toLowerCase().replace(/[^a-z0-9ğüşıöç-]/gi, '');
  return HOME_GAME_CATALOG.find((game) => [game.key, game.slug].includes(normalized)) || null;
}

export function getHomeGames() {
  return HOME_GAME_CATALOG.map((game) => ({ ...game, tags: [...game.tags] }));
}
