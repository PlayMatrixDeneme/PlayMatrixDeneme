const fs = require('fs');
const path = require('path');
const root = process.cwd();
const read = (p) => fs.readFileSync(path.join(root, p), 'utf8');
const checks = [];
function check(label, condition) { checks.push({ label, ok: !!condition }); }
const index = read('index.html');
const profileCss = read('public/css/profile.css');
const avatarCss = read('public/css/components/avatar-picker.css');
const frameCss = read('public/avatar-frame.css');
const legacy = read('public/js/home/legacy-home.runtime.js');
const profileStats = read('public/js/profile/profile-stats.js');
const routes = read('routes/profile.routes.js');
check('profile phase marker exists', index.includes('data-profile-phase="7"'));
check('big profile avatar host exists', index.includes('profileSheetAvatarShell') && index.includes('profile-main-avatar'));
check('uid field exists', index.includes('profileUidText'));
check('level badge exists', index.includes('profileLevelBadge'));
check('mc balance card exists', index.includes('profileMcBalance'));
check('xp value exists', index.includes('profileXpValue'));
check('email badge exists', index.includes('profileEmailBadge'));
check('username rights badge exists', index.includes('usernameRightsBadge'));
check('appearance frame state exists', index.includes('appearanceFrameState'));
check('profile save status exists', index.includes('profileSaveStatus'));
check('profile CSS phase present', profileCss.includes('FAZ 7 — Hesabım'));
check('profile stats 4-col CSS present', profileCss.includes('phase7-profile-stats'));
check('avatar picker locked CSS present', avatarCss.includes('Avatar seçici aktif/kilitli'));
check('frame picker locked CSS present', frameCss.includes('Çerçeve seçici premium'));
check('profile avatar size upgraded', frameCss.includes('#profileSheetAvatarShell { inline-size: 96px; block-size: 96px; }') || profileCss.includes('#profileSheetAvatarShell { inline-size: 96px'));
check('legacy stats uses Toplam Oyun', legacy.includes('Toplam Oyun'));
check('legacy profile save status patched', legacy.includes('profileSaveStatus'));
check('legacy uid sync patched', legacy.includes('profileUidText'));
check('profile stats module phase7', profileStats.includes('phase7-premium-profile-center'));
check('routes expose emailVerified', routes.includes('emailVerified: !!req.user.email_verified'));
check('routes update returns frameState', routes.includes('frameState: buildFrameState'));
const profileSection = (index.match(/data-sheet="profile"[\s\S]*?data-sheet="wheel"/) || [''])[0];
check('no betting terms in profile section', !/(Para Yatır|Para Çek|Bahis Geçmişi)/.test(profileSection));
const failed = checks.filter((item) => !item.ok);
checks.forEach((item, idx) => console.log((item.ok ? '✓' : '✗') + ' ' + String(idx + 1).padStart(2, '0') + ' ' + item.label));
if (failed.length) {
  console.error('\nFAZ 7 kontrolü başarısız: ' + failed.length + '/' + checks.length);
  process.exit(1);
}
console.log('\nFAZ 7 kontrolü başarılı: ' + checks.length + '/' + checks.length);
